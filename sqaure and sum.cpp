#include<stdio.h>
int main()
{ 
int i,j,k,t,m;
printf("Enter the natural numbers");
scanf("%d""%d",&i,&j);
for(;i>=0 && j>=0;);
{
	k=i+j;
	printf("\nThe sum of numbers is:%d\n",k);
	t=i*i;
	printf("\nThe square of first number is:%d\n",t);
	m=j*j;
	printf("\nThe square of second number is:%d\n",m);
	i--;
	j--;
	
}
return 0;
}

